        <br><br>
        <p>A rubber tree plant is also known as a Ficus elastica. These large trees can grow up to 50 feet (15 m.) tall.
            When learning how to care for a rubber tree plant, there are a few key things to remember, but rubber plant 
            care isn’t as difficult as one might think. Its beter to have a small young rubber tree to help it adapt as
            an indoor plant.</p>
            <h4>Light.</h4> 
            <p>When you have a rubber tree houseplant, it needs bright light but prefers indirect light that isn’t too hot.
            Some people recommend putting it near a window that has sheer curtains.</p>
            <h4>Water.</h5>
            <p>During the growing season, it needs to be kept moist. It is also a good idea to wipe off the leaves of your 
            rubber tree houseplant with a damp cloth or spritz it with water. If you water the rubber tree plant too much, 
            the leaves will turn yellow and brown and fall off.</p>
            <h4>Temperature</h4>
            <p>From 15°C to 21°C. It doesn't tolerate extreme cold.</p>
